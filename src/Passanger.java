public class Passanger {
    String plane;
    String name; 
    Integer age;
    String passportNum;
    String NIN;
    String gender; 

    Passanger(String plane, String name, Integer age, String passportNUM, String NIN, String gender){
        this.plane = plane;
        this.name = name;
        this.age = age; 
        this.passportNum = passportNUM; 
        this.NIN = NIN;
        this.gender = gender;
    }
}
